## == - сравнение
## > - больше
## < - меньше
## >= - больше или равно
## <= - меньше или равно
## != - не равно

number_1 = 10
number_2 = 20

if number_1 == number_2:
    print('Числа равны')
elif number_1 > number_2:
    print('Первое число больше второго')
elif number_1 < number_2:
    print('Первое число меньше второго')

if number_1 != number_2:
    print('Числа не равны')
